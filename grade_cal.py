def grade(mark):
    if mark >= 90:
        print("Grade: A")
    elif mark >= 80:
        print("Grade: B")
    elif mark>=70:
        print("Grade: C")
    elif mark >=60:
        print("Grade: D")
    elif mark >= 50:
        print("Grade: E")
    else:
        print("Grade: F")
        
grade(89)
grade(54)
grade(75)
grade(60)